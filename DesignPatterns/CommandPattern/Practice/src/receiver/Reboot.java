package receiver;

public class Reboot {
    public void dance() {
        System.out.println("機器人跳舞");
    }

    public void unDance() {
        System.out.println("機器人停止跳舞");
    }

    public void sing() {
        System.out.println("機器人唱歌");
    }

    public void unSing() {
        System.out.println("機器人停止唱歌");
    }

    public void sweep() {
        System.out.println("機器人打掃");
    }

    public void unSweep() {
        System.out.println("機器人停止打掃");
    }
}
